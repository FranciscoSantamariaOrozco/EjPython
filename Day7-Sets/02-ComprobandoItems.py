# Comprobando items en un set.

# Para comprobar si un item existe en un set es necesario utilizar el operador in.

Set = {"item1", "item2", "item3", "item4"}
print("El set contiene 'item3'?", "item3" in Set)
print()

Frutas = {"banana", "orange", "mango", "lemon"}
print("El set Frutas contiene la fruta 'mango'?", "mango" in Frutas)
print("El set Frutas contiene la fruta 'apple'?", "apple" in Frutas)
