print(2 + 3)        # Sumas     (+)
print(3 - 1)        # Restas    (-)
print(2 * 3)        # Multip.   (*)
print(3 / 2)        # División  (/)
print(3 ** 2)       # Exponente (**)
print(3 % 2)        # Modular   (%)
