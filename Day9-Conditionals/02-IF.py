# Condicional IF
# Tanto en Python como en otros lenguajes de programación, la palabra clave "if" es utilizada para comprobar
# si una condición es verdadera antes de ejecutar un bloque de código. Recuerde la sangría despues de los dos puntos.

# Syntax
"""
if condición:
    Esta parte del código funcionará si la condición es cierta.
"""

A = 3
if A > 0:
    print("A es un número positivo.")
print()

